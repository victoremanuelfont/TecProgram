package planoDoJogo;

public class Posicao {
	
	public int posicaoX;
	public int posicaoY;
	
	public Posicao(int posicaoX, int posicaoY) {
		this.posicaoX = posicaoX;
		this.posicaoY = posicaoY;
	}

	
	public void atualizaValores(int posicaoX, int posicaoY) {
		this.posicaoX = posicaoX;
		this.posicaoY = posicaoY;
	}


	
	
}
